public class TypeCasting {
    public static void main(String[] args) {

        //Type Casting (1) from float to int>>
        float a = 10.56f;
        int b = (int)a;  // assigning integer a into float b
        System.out.println(b);
        System.out.println(a);

        // Type Casting (2)>>
        char ch = 'a';
        int num = ch;  //assigning a integer val of a in num
        System.out.println(num);

    }
}


