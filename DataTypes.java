public class DataTypes {
    public static void main(String[] args){
        
        byte b = 5; // Byte Data Type (1 byte)
        System.out.println(b);

        short s = 50; // Short Data Type (2 bytes)
        System.out.println(s);

        int num = 10; // Intger Data Type (4 bytes)
        System.out.println(num);

        long lng = 10000; // Long Data Type (8 bytes)
        System.out.println(lng);

        char c = 'a'; // Character Data Type (2 bytes)
        System.out.println(c);

        float pi = 3.14f; // Float Data Type (4 bytes)
        System.out.println(pi);

        boolean bool = true; //Boolean Data Type (1 byte)
        System.out.println(bool);

        double db = 91.6775475; //Double  Data Type (8 bytes)
        System.out.println(db);



    }
}
