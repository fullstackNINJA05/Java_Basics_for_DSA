public class TypeConversion {
    public static void main(String[] args) {

        //Type Conversion (1) from int to float>>
        int a = 10;
        float b = a;  // assigning integer a into float b
        System.out.println(b);


        //Type Conversion (2) from char c into integer n>>
        char ch = 'c';
        int n = ch;    //converting char c into integer value through type conversion
        System.out.println(n);

    }
}
