public class Variables {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println(a);
        System.out.println(b);

        String str = "Prem Panchal";
        System.out.println(str);

        a = b;
        a += 10;
        System.out.println(b);
        System.out.println(a);
    }
}
